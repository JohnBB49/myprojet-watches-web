package com.myproject.entity;

public class Category {
	
	private long cateId;
	
	private String cateName;

	public long getCateId() {
		return cateId;
	}

	public void setCateId(long cateId) {
		this.cateId = cateId;
	}

	public String getCateName() {
		return cateName;
	}

	public void setCateName(String name) {
		this.cateName = name;
	}

}
